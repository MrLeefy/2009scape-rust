/// Draw text at pixel position.
pub fn draw_text(r2d: &mut Renderer2D, text: &str, x: f32, y: f32, color: [f32; 4], scale: f32) {
    let cw = 6.0 * scale;
    for (ci, ch) in text.chars().enumerate() {
        let uc = ch.to_ascii_uppercase();
        if let Some((_, rows)) = GLYPHS.iter().find(|(c, _)| *c == uc) {
            let cx = x + ci as f32 * cw;
            for row in 0..7usize {
                for col in 0..5usize {
                    if (rows[row] >> (4 - col)) & 1 != 0 {
                        r2d.fill_rect(cx + col as f32 * scale, y + row as f32 * scale, scale, scale, color);
                    }
                }
            }
        }
    }
}

/// Draw text with drop shadow.
pub fn draw_shadow(r2d: &mut Renderer2D, text: &str, x: f32, y: f32, color: [f32; 4], scale: f32) {
    draw_text(r2d, text, x + scale, y + scale, [0.0, 0.0, 0.0, color[3] * 0.6], scale);
    draw_text(r2d, text, x, y, color, scale);
}

/// Measure text width in pixels.
pub fn text_width(text: &str, scale: f32) -> f32 { text.len() as f32 * 6.0 * scale }

/// Draw centered text with shadow.
pub fn draw_centered(r2d: &mut Renderer2D, text: &str, cx: f32, y: f32, color: [f32; 4], scale: f32) {
    draw_shadow(r2d, text, cx - text_width(text, scale) / 2.0, y, color, scale);
}
